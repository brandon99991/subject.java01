public class CharTest03 {
    public static void main(String[] args) {
        // char를 배열을 이용하여 문자열 형태로 찍어보기
        char[] test1 = {'H', 'e', 'l', 'l', 'o'};
        System.out.println("test1의 배열을 찍어보기? ");    // 출력: A
        for (int i=0; i < test1.length; i++) {
            System.out.print(test1[i]); // Hello 출력
        }        
        System.out.println("");           
        System.out.println("------------------------------------------");   
    }
}




